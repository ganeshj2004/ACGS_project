export function generateJavaStack(meta) {
  const parent = meta.parent || {};
  const table = parent.table || "YOUR_TABLE";
  const className = table.split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase()).join('');

  return `
/**
 * GENERATED JAVA (SPRING BOOT) STACK FOR: ${table}
 * Created at: ${new Date().toLocaleString()}
 */

@RestController
@RequestMapping("/api/${table.toLowerCase()}")
public class ${className}Controller {

    @Autowired
    private JdbcTemplate jdbcTemplate;

    @GetMapping
    public List<Map<String, Object>> getAll() {
        return jdbcTemplate.queryForList("SELECT * FROM ${table}");
    }

    @PostMapping("/save")
    public ResponseEntity<?> save(@RequestBody Map<String, Object> data) {
        // Calling the SQL Stored Procedure generated by CodeGen Pro
        jdbcTemplate.execute("CALL ${parent.name}(...)");
        return ResponseEntity.ok("Record saved successfully via Spring Boot");
    }
}
`;
}
